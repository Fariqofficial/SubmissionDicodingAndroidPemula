package pradita.id.ac.submissiondicodingandroidpemula.model;

public class Warrior {

    private String heroWarrior, roleWarrior, storyHeroWarrior;
    private int gambarHeroWarrior;

    public String getHeroWarrior() {
        return heroWarrior;
    }

    public void setHeroWarrior(String heroWarrior) {
        this.heroWarrior = heroWarrior;
    }

    public String getRoleWarrior() {
        return roleWarrior;
    }

    public void setRoleWarrior(String roleWarrior) {
        this.roleWarrior = roleWarrior;
    }

    public String getStoryHeroWarrior() {
        return storyHeroWarrior;
    }

    public void setStoryHeroWarrior(String storyHeroWarrior) {
        this.storyHeroWarrior = storyHeroWarrior;
    }

    public int getGambarHeroWarrior() {
        return gambarHeroWarrior;
    }

    public void setGambarHeroWarrior(int gambarHeroWarrior) {
        this.gambarHeroWarrior = gambarHeroWarrior;
    }
}
