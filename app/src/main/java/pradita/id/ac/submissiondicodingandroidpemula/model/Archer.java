package pradita.id.ac.submissiondicodingandroidpemula.model;

public class Archer {

    private String nama_archer, role_hero, story_archer;
    private int image_hero;

    public String getNama_archer() {
        return nama_archer;
    }

    public void setNama_archer(String nama_archer) {
        this.nama_archer = nama_archer;
    }

    public String getRole_hero() {
        return role_hero;
    }

    public void setRole_hero(String role_hero) {
        this.role_hero = role_hero;
    }

    public int getImage_hero() {
        return image_hero;
    }

    public void setImage_hero(int image_hero) {
        this.image_hero = image_hero;
    }

    public String getStory_archer() {
        return story_archer;
    }

    public void setStory_archer(String story_archer) {
        this.story_archer = story_archer;
    }
}
