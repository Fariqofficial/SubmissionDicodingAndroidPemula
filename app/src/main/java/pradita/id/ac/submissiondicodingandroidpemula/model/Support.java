public class Support {

    private String namaHeroSup, roleHeroSup, storyHeroSup;
    private int supportImage;

    public String getNamaHeroSup() {
        return namaHeroSup;
    }

    public void setNamaHeroSup(String namaHeroSup) {
        this.namaHeroSup = namaHeroSup;
    }

    public String getRoleHeroSup() {
        return roleHeroSup;
    }

    public void setRoleHeroSup(String roleHeroSup) {
        this.roleHeroSup = roleHeroSup;
    }

    public String getStoryHeroSup() {
        return storyHeroSup;
    }

    public void setStoryHeroSup(String storyHeroSup) {
        this.storyHeroSup = storyHeroSup;
    }

    public int getSupportImage() {
        return supportImage;
    }

    public void setSupportImage(int supportImage) {
        this.supportImage = supportImage;
    }
}
