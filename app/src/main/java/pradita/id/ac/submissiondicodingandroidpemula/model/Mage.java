package pradita.id.ac.submissiondicodingandroidpemula.model;

public class Mage {

    private String hero_mage, role_mage, kisah_mage;
    private int gambarMage;

    public String getHero_mage() {
        return hero_mage;
    }

    public void setHero_mage(String hero_mage) {
        this.hero_mage = hero_mage;
    }

    public String getRole_mage() {
        return role_mage;
    }

    public void setRole_mage(String role_mage) {
        this.role_mage = role_mage;
    }

    public String getKisah_mage() {
        return kisah_mage;
    }

    public void setKisah_mage(String kisah_mage) {
        this.kisah_mage = kisah_mage;
    }

    public int getGambarMage() {
        return gambarMage;
    }

    public void setGambarMage(int gambarMage) {
        this.gambarMage = gambarMage;
    }
}
